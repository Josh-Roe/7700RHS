void driveVolts(float lspeed, float rspeed, int wt) {
  lspeed = lspeed * 120;
  rspeed = rspeed * 120;
  RFD.spin(fwd, rspeed, voltageUnits::mV);
  RMD.spin(fwd, rspeed, voltageUnits::mV);
  RBD.spin(fwd, rspeed, voltageUnits::mV);
  LFD.spin(fwd, lspeed, voltageUnits::mV);
  LMD.spin(fwd, lspeed, voltageUnits::mV);
  LBD.spin(fwd, lspeed, voltageUnits::mV);
  wait(wt, msec);
}

